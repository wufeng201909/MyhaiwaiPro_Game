package com.sdk.mysdklibrary.walletconnect

import com.wemadetree.wemixauth.a2a.data.*

class WemixHash {
    companion object{
        fun getListFTData(contract: String,useraddress: String,nonce: String,money:String,column:String) :List<SignData>{
            return listOf(SignData(
                    SignType.MESSAGE,
                    HashData.Message(
                            listOf(
                                    MessageData(
                                            "address",
                                            contract,
                                            "contract"
                                    ),
                                    MessageData(
                                            "address",
                                            useraddress,
                                            "user"
                                    ),
                                    MessageData(
                                            "uint256",
                                            nonce,
                                            "nonce"
                                    ),
                                    MessageData(
                                            "bytes32",
                                            column,
                                            "column"
                                    ),
                                    MessageData(
                                            "uint256",
                                            money,
                                            "amount"
                                    )
                            )
                    ),
                    Memo("MESSAGE memo", MemoTheme.YELLOW)
            ))
        }

        fun getHashData(hash: String) :List<SignData>{
            return listOf(
                    SignData(
                            SignType.TRANSACTION,
                            HashData.Hash(hash),
                            Memo("MESSAGE memo", MemoTheme.YELLOW)
                    )
            )
        }

        fun getListNFTData(contract: String,useraddress: String,fee: String,nonce:String) :List<SignData>{
            return listOf(SignData(
                    SignType.MESSAGE,
                    HashData.Message(
                            listOf(
                                    MessageData(
                                            "address",
                                            contract,
                                            "contract"
                                    ),
                                    MessageData(
                                            "uint",
                                            fee,
                                            "fee"
                                    ),
                                    MessageData(
                                            "address",
                                            useraddress,
                                            "user"
                                    ),
                                    MessageData(
                                            "uint",
                                            nonce,
                                            "nonce"
                                    ),
                                    MessageData(
                                            "bool",
                                            "false",
                                            "isTransferable"
                                    )
                            )
                    ),
                    Memo("MESSAGE memo", MemoTheme.YELLOW)
            ))
        }
    }
}